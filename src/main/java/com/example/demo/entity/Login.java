package com.example.demo.entity;

public class Login {
	
	private String code;
	private String password;
	
	public Login() {
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
}
